package p79068.crypto.hash;

import org.junit.Test;


public final class KeccakTest {
	
	private static final String msg0 = "";
	private static final String msg1 = "The quick brown fox jumps over the lazy dog";
	private static final String msg2 = "The quick brown fox jumps over the lazy dog.";
	
	
	@Test
	public void testKeccak224() {
		HashUtils.testAscii(Keccak.KECCAK_224_FUNCTION, msg0, "F71837502BA8E10837BDD8D365ADB85591895602FC552B48B7390ABD");
		HashUtils.testAscii(Keccak.KECCAK_224_FUNCTION, msg1, "310AEE6B30C47350576AC2873FA89FD190CDC488442F3EF654CF23FE");
		HashUtils.testAscii(Keccak.KECCAK_224_FUNCTION, msg2, "C59D4EAEAC728671C635FF645014E2AFA935BEBFFDB5FBD207FFDEAB");
	}
	
	
	@Test
	public void testKeccak256() {
		HashUtils.testAscii(Keccak.KECCAK_256_FUNCTION, msg0, "C5D2460186F7233C927E7DB2DCC703C0E500B653CA82273B7BFAD8045D85A470");
		HashUtils.testAscii(Keccak.KECCAK_256_FUNCTION, msg1, "4D741B6F1EB29CB2A9B9911C82F56FA8D73B04959D3D9D222895DF6C0B28AA15");
		HashUtils.testAscii(Keccak.KECCAK_256_FUNCTION, msg2, "578951E24EFD62A3D63A86F7CD19AAA53C898FE287D2552133220370240B572D");
	}
	
	
	@Test
	public void testKeccak384() {
		HashUtils.testAscii(Keccak.KECCAK_384_FUNCTION, msg0, "2C23146A63A29ACF99E73B88F8C24EAA7DC60AA771780CCC006AFBFA8FE2479B2DD2B21362337441AC12B515911957FF");
		HashUtils.testAscii(Keccak.KECCAK_384_FUNCTION, msg1, "283990FA9D5FB731D786C5BBEE94EA4DB4910F18C62C03D173FC0A5E494422E8A0B3DA7574DAE7FA0BAF005E504063B3");
		HashUtils.testAscii(Keccak.KECCAK_384_FUNCTION, msg2, "9AD8E17325408EDDB6EDEE6147F13856AD819BB7532668B605A24A2D958F88BD5C169E56DC4B2F89FFD325F6006D820B");
	}
	
	
	@Test
	public void testKeccak512() {
		HashUtils.testAscii(Keccak.KECCAK_512_FUNCTION, msg0, "0EAB42DE4C3CEB9235FC91ACFFE746B29C29A8C366B7C60E4E67C466F36A4304C00FA9CAF9D87976BA469BCBE06713B435F091EF2769FB160CDAB33D3670680E");
		HashUtils.testAscii(Keccak.KECCAK_512_FUNCTION, msg1, "D135BB84D0439DBAC432247EE573A23EA7D3C9DEB2A968EB31D47C4FB45F1EF4422D6C531B5B9BD6F449EBCC449EA94D0A8F05F62130FDA612DA53C79659F609");
		HashUtils.testAscii(Keccak.KECCAK_512_FUNCTION, msg2, "AB7192D2B11F51C7DD744E7B3441FEBF397CA07BF812CCEAE122CA4DED6387889064F8DB9230F173F6D1AB6E24B6E50F065B039F799F5592360A6558EB52D760");
	}
	
	
	@Test
	public void testZeroize() {
		HashUtils.testZeroization(Keccak.KECCAK_224_FUNCTION);
		HashUtils.testZeroization(Keccak.KECCAK_256_FUNCTION);
		HashUtils.testZeroization(Keccak.KECCAK_384_FUNCTION);
		HashUtils.testZeroization(Keccak.KECCAK_512_FUNCTION);
	}
	
}
